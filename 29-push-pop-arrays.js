// 1. push() -> adds a new alement at the end of the array and returns the final lenght
const fruits = ['apple','banana','orange']
console.log(fruits)
const newFruitsLenght = fruits.push('almonds')
console.log(fruits)
console.log(newFruitsLenght)

// 2. pop() -> removes the last element of an array and returns it
console.log(fruits)
const lastFruitRemoved = fruits.pop()
console.log(fruits)
console.log(lastFruitRemoved)